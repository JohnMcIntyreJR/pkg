#' @title A function for subtracting
#'
#' @description A really good subtracting function.
#' Perhaps the best function ever!
#'
#' A work of pure genius.
#' @param x a number
#' @param y another number
#' @return a number
#' @export
#' @examples
#' subtract(5, 10)
#' ## Can also use negative numbers
#' subtract(-5, 10)
#' @export
